W latach trzydziestych XXI wieku,  odbyło się globalne referendum. W nim obywatele ziemi
zadecydowali, że oddają władzę z rąk skostniałych i nieefektywnych struktur państwowych.  
Po plebiscycie władzę objęli faktyczni posiadacze większości majątku i dóbr planety. 
Korporacje. 
W Europie największe wpływy miała Candle Corporation. Stawiająca na rozwój nie bacząc na 
jego koszty. Mieszkańcy czuli się swobodnie i żyli dostatnio. Wiedzieli jednak, że najgorsze co 
mogliby uczynić, to wejść w drogę planom Candle.
